package cn.nullah.platform.base.pojo;

public class CfgInf {
	private Integer id;
	private String name;

	public CfgInf() {
		super();
	}

	public CfgInf(String name) {
		super();
		this.name = name;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
